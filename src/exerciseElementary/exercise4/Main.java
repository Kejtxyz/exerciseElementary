package exerciseElementary.exercise4;
/**Przygotuj program, który wyświetli na konsoli liczby od 1 do 49. Użyj w tym celu pętli
 while*/
public class Main {
    public static void main(String[] args) {
        int a = 1;
        while (a <= 49){
            System.out.println(a);
            a++;
        }
    }
}
