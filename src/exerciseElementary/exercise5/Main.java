package exerciseElementary.exercise5;
/**Przygotuj program, który wyświetli na konsoli liczby od 1 do 49. Użyj w tym celu pętli
 do ... while*/
public class Main {
    public static void main(String[] args) {
        int a = 1;
        do {
            System.out.println(a);
            a++;
        }
        while (a <= 49);
    }
}
